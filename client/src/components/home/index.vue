export default {
  data() {
    return {
    };
  },

  created() {
    exampleService.query()
      .then((result) => {
        console.log('EXAMPLE API REQUEST FINISHED');
      })
      .catch((err) => {
        console.log(err);
      });

    authService.login('username', 'password')
      .then((result) => {
        console.log('USER HAS BEEN AUTHENTICATED!!');
        setUserAccessToken(result.data.token);

        authService.refreshToken()
          .then((r) => {
            console.log('USER TOKEN HAS BEEN REFRESHED!!');
          })
          .catch((err) => {
            console.log(err.toJSON());
          });

        authService.getUser()
          .then((r) => {
            console.log('GOT USER!!');
          })
          .catch((err) => {
            console.log(err.toJSON());
          });

        authExampleService.query()
          .then((result) => {
            console.log('AUTHENTICATED EXAMPLE API REQUEST FINISHED');
          })
          .catch((err) => {
            console.log(err.toJSON());
          });

          authService.logout()
            .then((result) => {
              console.log('USER HAS BEEN LOGGED OUT');
            })
          .catch((err) => {
            console.log(err.toJSON());
          });
      })
      .catch((err) => {
        console.log(err);
      });
